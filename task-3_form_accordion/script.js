let full1 = document.getElementById("aa");
let a1 = 0;
function flower() {
  a1++;
  if (a1 % 2 == 0) {
    full1.style.transition = ".2s all ease";
    full1.style.marginTop = "-478px";
    full1.style.zIndex = "-2";
  } else {
    full1.style.zIndex = "1";
    full1.style.transition = ".9s all ease";
    full1.style.marginTop = "0px";
  }
}
